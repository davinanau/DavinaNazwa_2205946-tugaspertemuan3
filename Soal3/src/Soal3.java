public class Soal3 {
    public static void main(String[] args) {
        char charA = 'A';
        char charB = 'B';

        int asciiA = (int) charA;
        int asciiB = (int) charB;

        int totalAscii = asciiA + asciiB;

        System.out.println("Jumlah kode ASCII dari karakter 'A' dan 'B' adalah: " + totalAscii);
    }
}
